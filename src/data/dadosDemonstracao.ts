const calcPrazo = (dataInicio: Date, prazoFinal: Date): ClassificacaoPrazoMeta => {
  const meses = differenceInDays(prazoFinal, dataInicio) / 30.4;
  if (meses <= 12) return 'curto prazo';
  if (meses <= 36) return 'médio prazo';
  return 'longo prazo';
};

